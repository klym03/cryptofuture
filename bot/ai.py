def fix_html_tags(content: str) -> str:
    """
    –í–∏–ø—Ä–∞–≤–ª—è—î –Ω–µ–∑–∞–∫—Ä–∏—Ç—ñ —Ç–∞ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ñ HTML —Ç–µ–≥–∏ –≤ –∫–æ–Ω—Ç–µ–Ω—Ç—ñ
    """
    if not content:
        return content
    
    # –°–ø–æ—á–∞—Ç–∫—É –≤–∏–ø—Ä–∞–≤–ª—è—î–º–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ñ —Ç–µ–≥–∏ —Ç–∏–ø—É </b</b> -> </b>
    content = re.sub(r'</(\w+)</\1>', r'</\1>', content)
    
    # –í–∏–ø—Ä–∞–≤–ª—è—î–º–æ –ø–æ–¥–≤—ñ–π–Ω—ñ —Ç–µ–≥–∏ —Ç–∏–ø—É </b></b> -> </b>
    content = re.sub(r'</(\w+)></\1>', r'</\1>', content)
    
    # –í–∏–ø—Ä–∞–≤–ª—è—î–º–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ñ –∑–∞–∫—Ä–∏–≤–∞—é—á—ñ —Ç–µ–≥–∏ —Ç–∏–ø—É </b</b> -> </b>
    content = re.sub(r'</(\w+)</', r'</\1>', content)
    
    # –†–∞—Ö—É—î–º–æ –≤—ñ–¥–∫—Ä–∏—Ç—ñ —Ç–∞ –∑–∞–∫—Ä–∏—Ç—ñ —Ç–µ–≥–∏
    open_tags = {}
    
    # –ó–Ω–∞—Ö–æ–¥–∏–º–æ –≤—Å—ñ –ø—Ä–∞–≤–∏–ª—å–Ω—ñ —Ç–µ–≥–∏
    tag_pattern = r'<(/?)(\w+)(?:\s[^>]*)?>'
    matches = list(re.finditer(tag_pattern, content))
    
    for match in matches:
        is_closing, tag_name = match.groups()
        
        if is_closing:
            # –ó–∞–∫—Ä–∏–≤–∞—é—á–∏–π —Ç–µ–≥
            if tag_name in open_tags and open_tags[tag_name] > 0:
                open_tags[tag_name] -= 1
        else:
            # –í—ñ–¥–∫—Ä–∏–≤–∞—é—á–∏–π —Ç–µ–≥
            open_tags[tag_name] = open_tags.get(tag_name, 0) + 1
    
    # –î–æ–¥–∞—î–º–æ –∑–∞–∫—Ä–∏–≤–∞—é—á—ñ —Ç–µ–≥–∏ –¥–ª—è –Ω–µ–∑–∞–∫—Ä–∏—Ç–∏—Ö
    for tag_name, count in open_tags.items():
        if count > 0:
            content += f'</{tag_name}>' * count
            logging.warning(f"–î–æ–¥–∞–Ω–æ {count} –∑–∞–∫—Ä–∏–≤–∞—é—á–∏—Ö —Ç–µ–≥—ñ–≤ –¥–ª—è <{tag_name}>")
    
    # –í–∏–¥–∞–ª—è—î–º–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ñ —Ç–µ–≥–∏, —è–∫—ñ –Ω–µ –≤–¥–∞–ª–æ—Å—è –≤–∏–ø—Ä–∞–≤–∏—Ç–∏
    content = re.sub(r'<[^>]*<[^>]*>', '', content)
    
    return content

